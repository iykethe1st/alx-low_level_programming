#include <stdio.h>
/**
 * main - Entry Point
 * Return: Always 0 (Success)
 */

int main (void)
{
	printf("The size of a char is: %i bytes\n", sizeof(char));
	printf("The size of an int is: %i bytes\n", sizeof(int));
	printf("The size of a float is: %i bytes\n", sizeof(float));
	printf("The size of double data type is: %i bytes\n", sizeof(double));
	return (0);
}
